    /**
     * Handle the Project "updated" event.
     *
     * @param  \App\Models\Project  $project
     * @return void
     */
    public function updated(Project $project)
    {
        if ($project->projectStatus->name == 'Completed') {
            $project->completed_at = now();
            $project->saveQuietly();
        } else {
            $project->completed_at = null;
            $project->saveQuietly();
        }
    }

